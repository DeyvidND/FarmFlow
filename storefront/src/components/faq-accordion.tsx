export function FaqAccordion() {
  const [open, setOpen] = useState(0);

  return (
    <div className="acc" style={{ marginTop: 32 }}>
      {FAQ.map((f, i) => (
        <div className={`acc__item${open === i ? ' open' : ''}`} key={i}>
          <button
            type="button"
            className="acc__head"
            onClick={() => setOpen(open === i ? -1 : i)}
            aria-expanded={open === i}
          >
            <span>{f.q}</span>
            <span className="ico">{open === i ? '−' : '+'}</span>
          </button>
          <div className="acc__body">
            <div className="acc__body-inner">{f.a}</div>
          </div>
        </div>
      ))}
    </div>
  );
}
